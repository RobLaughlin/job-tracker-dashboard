import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const schemasDir = path.resolve(__dirname, "../../schemas");
const outputPath = path.resolve(
  __dirname,
  "../src/generated/schema-registry.ts",
);
const checkMode = process.argv.includes("--check");

async function collectSchemaFiles(root, subpath = "") {
  const dirPath = path.join(root, subpath);
  const entries = await readdir(dirPath, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    if (entry.name.startsWith(".")) {
      continue;
    }

    const nextSubpath = path.join(subpath, entry.name);
    if (entry.isDirectory()) {
      const nested = await collectSchemaFiles(root, nextSubpath);
      files.push(...nested);
    } else if (entry.isFile() && entry.name.endsWith(".json")) {
      files.push(nextSubpath);
    }
  }

  return files;
}

function createSource(fileEntries) {
  const registryLines = fileEntries.map(({ key, schema }) => {
    const serialized = JSON.stringify(schema, null, 2)
      .split("\n")
      .map((line) => `  ${line}`)
      .join("\n");
    return `  ${JSON.stringify(key)}: ${serialized},`;
  });

  const schemaPathLiterals = fileEntries
    .map(({ key }) => `  ${JSON.stringify(key)},`)
    .join("\n");

  const streamSchemaPath = "sse/stream-event.schema.json";
  const hasStreamSchema = fileEntries.some(
    ({ key }) => key === streamSchemaPath,
  );
  if (!hasStreamSchema) {
    throw new Error(`Missing required schema: ${streamSchemaPath}`);
  }

  return [
    "// This file is auto-generated by scripts/generate-validators.mjs.",
    "// Do not edit manually.",
    "",
    "export const schemaRegistry = {",
    registryLines.join("\n"),
    "} as const",
    "",
    "export const schemaPaths = [",
    schemaPathLiterals,
    "] as const",
    "",
    "export type SchemaPath = (typeof schemaPaths)[number]",
    "",
    `export const streamEventSchemaPath = ${JSON.stringify(streamSchemaPath)} as const`,
    "",
  ].join("\n");
}

async function main() {
  const files = (await collectSchemaFiles(schemasDir)).sort((a, b) =>
    a.localeCompare(b),
  );
  const fileEntries = [];

  for (const relativeFile of files) {
    const absoluteFile = path.join(schemasDir, relativeFile);
    const raw = await readFile(absoluteFile, "utf8");
    const schema = JSON.parse(raw);
    const key = relativeFile.split(path.sep).join("/");
    fileEntries.push({ key, schema });
  }

  const generated = createSource(fileEntries);

  let current = "";
  try {
    current = await readFile(outputPath, "utf8");
  } catch {
    current = "";
  }

  if (checkMode) {
    if (current !== generated) {
      throw new Error(
        `Generated validators are stale: ${path.relative(process.cwd(), outputPath)}`,
      );
    }
    return;
  }

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, generated, "utf8");
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
